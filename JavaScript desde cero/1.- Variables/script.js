// Para inicializar una variable en JavaScript simplemente utilizaremos los comandos let, var o const dependiendo del caso
// *var
// El comando var define una variable global (actualmente poco usado, o usado en ocasiones especificas)

var global1;
global1 = 'Variable Global';

// *let
// El comando let define una variable regional

let regional;
regional = 'variable regional';

// *const
// El comando const define una variable constante (el valor que se le sea asignado sera el valor permanente) ademas su valor obligatoriamente debera ser inicializado al mismo tiempo de su definicion

// const constante; //Da error
const constante = 'Variable Constante';

// Se puede definir e inicializar mas de una variable al mismo tiempo
